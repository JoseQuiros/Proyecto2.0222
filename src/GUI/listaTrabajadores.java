    public listaTrabajadores() {
        initComponents();
        String[] columnasTrabajadores = {
            "Nombre:",
            "Edad:",
            "Celular:",
            "Usuario:",
            "Contrase√±a:"
        };
        String rutaArchivo = "Trabajadores.txt";
        File fileTrabajador = new File(rutaArchivo);

        int filasTrabajadores = 0;

        try {
            if (!fileTrabajador.exists()) {
                fileTrabajador.createNewFile();
            }
            FileReader fileR = new FileReader(fileTrabajador);
            BufferedReader buffReader = new BufferedReader(fileR);
            while ((buffReader.readLine()) != null) {

                filasTrabajadores++;
            }
        } catch (Exception e) {
        }

        Object[][] datosTrabajadores = new Object[filasTrabajadores][5];
        try {
            if (!fileTrabajador.exists()) {
                fileTrabajador.createNewFile();
            }

            String cadena = "";
            FileReader fileR = new FileReader(fileTrabajador);
            BufferedReader buffReader = new BufferedReader(fileR);

            filasTrabajadores = 0;

            while ((cadena = buffReader.readLine()) != null) {
                if (cadena.indexOf(",") != -1) {
                    datosTrabajadores[filasTrabajadores][0] = cadena.split(",")[0];
                    datosTrabajadores[filasTrabajadores][1] = cadena.split(",")[1];
                    datosTrabajadores[filasTrabajadores][2] = cadena.split(",")[2];
                    datosTrabajadores[filasTrabajadores][3] = cadena.split(",")[3];
                    datosTrabajadores[filasTrabajadores][4] = cadena.split(",")[4];
                }
                filasTrabajadores++;
            }
        } catch (Exception e) {
        }

        JTable tableDiario = new JTable(datosTrabajadores, columnasTrabajadores);
        this.setLayout(new BorderLayout());
        this.add(tableDiario.getTableHeader(), BorderLayout.PAGE_START);
        this.add(tableDiario, BorderLayout.CENTER);

    }

